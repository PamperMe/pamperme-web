function Babysitter(babysitter) {
    this.name = babysitter.name;
    this.email = babysitter.email;
    this.age = babysitter.age;
    this.location = babysitter.location;
    this.evaluation = babysitter.evaluation;
    this.price = babysitter.price;
    this.description = babysitter.description;
    this.phone = babysitter.phone;
}


module.exports = Babysitter;
