var id, uid, name, address, phone, email, description;

function User(client){
    this.name = client.name;
    this.id = client.id;
    this.uid.client.uid;
    this.address = client.address;
    this.phone = client.phone;
    this.email = client.email;
    this.description = client.description;
}

module.exports = User;
